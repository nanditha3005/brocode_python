#to calculate circumference of circle  c=2*pi*r

import math
radius=float(input('Enter the radius of circle:'))          #10.5
circumference=2*math.pi *radius
print(f"the circumference is{round(circumference,2)}cm²")   #65.97cm
print(f"the circumference is{circumference}cm²")            #65.97344572538566cm²

